// Map sirve para clonar un arreglo
// cb o callback sirve para recorrer y clonar un array

function map(arr,cb){
    if(!Array.isArray(arr)){
        throw new TypeError("Arg isnt an array")
    }

    if(typeof cb !=='function'){
        throw new TypeError("cb isnt an function")
    }
    const newArr=[]

    for(let i=0; i<arr.length;i++){
        newArr[i]=cb(arr[i])
    }
    return newArr
}

